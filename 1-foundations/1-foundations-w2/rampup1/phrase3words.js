function phrase3words(value1, value2, value3) {
    var answer = value1 + " " + value2 + " " + value3;  
    return answer;
}

var result1 = phrase3words("smile","at","everyone");
print(result1);
var result2 = phrase3words("everyone","wave", "back");
print(result2);
var result3 = phrase3words("coding","is", "fun");
print(result3);